# Q1: Golden angle helps for STRUCTURED vectors?
# ============================================================
def experiment_q1():
    """Does golden angle outperform random angles for random-walk (structured) vectors?"""
    print("=" * 60)
    print("Q1: Golden angle for structured (random walk) vectors")
    print("=" * 60)
    
    random.seed(42)
    N = 1000
    dim = 64
    
    # Generate random-walk vectors (structured: cumulative steps)
    def gen_walk():
        v = [0.0] * dim
        for i in range(dim):
            step = random.gauss(0, 1)
            # Random walk: each component depends on previous
            v[i] = v[i-1] * 0.8 + step if i > 0 else step
        return v
    
    walks = [gen_walk() for _ in range(N)]
    
    # Golden projection
    golden_sims = []
    for w in walks:
        w_noisy = [x + random.gauss(0, 0.1) for x in w]
        p1 = project_embedding(w, angle_mult=PHI)
        p2 = project_embedding(w_noisy, angle_mult=PHI)
        d = math.hypot(p1[0]-p2[0], p1[1]-p2[1])
        golden_sims.append(d)
    
    # Random irrational angles
    random_angles = [math.sqrt(2), math.e, math.pi, math.sqrt(3), math.sqrt(7),
                     math.log(2)*10, math.sqrt(11), math.sqrt(13), math.e**0.5, math.pi**0.7]
    random_sims = []
    for angle in random_angles:
        sims = []
        for w in walks:
            w_noisy = [x + random.gauss(0, 0.1) for x in w]
            p1 = project_embedding(w, angle_mult=angle)
            p2 = project_embedding(w_noisy, angle_mult=angle)
            d = math.hypot(p1[0]-p2[0], p1[1]-p2[1])
            sims.append(d)
        random_sims.append(sum(sims) / len(sims))
    
    golden_mean = sum(golden_sims) / len(golden_sims)
    random_mean = sum(random_sims) / len(random_sims)
    
    print(f"  Golden mean distance: {golden_mean:.6f}")
    print(f"  Random angles mean: {random_mean:.6f}")
    print(f"  Ratio (golden/random): {golden_mean/random_mean:.4f}")
    
    # Lower distance = better noise resistance = better for structured data
    golden_wins = golden_mean < random_mean
    print(f"  VERDICT: Golden angle {'WINS' if golden_wins else 'LOSES'} for structured vectors")
    print()
    return {"question": "Q1", "golden_wins": golden_wins,
            "golden_mean": golden_mean, "random_mean": random_mean}

