# Q10: Snap errors are spatially localized?
# ============================================================
def experiment_q10():
    """When a snap error occurs, is the wrong tile at least nearby?"""
    print("=" * 60)
    print("Q10: Snap errors are spatially localized")
    print("=" * 60)
    
    random.seed(42)
    N = 1000
    
    closer_to_perturbed = 0
    closer_to_original = 0
    total_errors = 0
    
    for _ in range(N):
        emb = [random.gauss(0, 1) for _ in range(64)]
        proj = project_embedding(emb)
        correct_tile = snap_to_lattice((proj[0], proj[1]))
        
        # Large perturbation to trigger snap errors
        emb_noisy = [e + random.gauss(0, 2.0) for e in emb]
        proj_noisy = project_embedding(emb_noisy)
        snapped_tile = snap_to_lattice((proj_noisy[0], proj_noisy[1]))
        
        if snapped_tile != correct_tile:
            total_errors += 1
            # Distance from perturbed projection to snapped tile
            d_snapped = math.hypot(proj_noisy[0] - snapped_tile[0]*PHI,
                                    proj_noisy[1] - snapped_tile[1]*PHI)
            # Distance from perturbed projection to correct tile
            d_correct = math.hypot(proj_noisy[0] - correct_tile[0]*PHI,
                                    proj_noisy[1] - correct_tile[1]*PHI)
            if d_snapped <= d_correct:
                closer_to_perturbed += 1
            else:
                closer_to_original += 1
    
    localization_rate = closer_to_perturbed / total_errors if total_errors > 0 else 0
    error_rate = total_errors / N
    
    print(f"  Error rate: {error_rate:.4f} ({total_errors}/{N})")
    print(f"  Localization rate: {localization_rate:.4f} ({closer_to_perturbed}/{total_errors})")
    print(f"  Errors where snapped tile is closer to perturbed: {closer_to_perturbed}")
    print(f"  Errors where correct tile is closer: {closer_to_original}")
    
    localized = localization_rate > 0.5
    print(f"  VERDICT: Snap errors are {'SPATIALLY LOCALIZED' if localized else 'NOT LOCALIZED'}")
    print()
    return {"question": "Q10", "localized": localized,
            "error_rate": error_rate, "localization_rate": localization_rate}

