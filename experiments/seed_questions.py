# Q8: 3-coloring as retrieval cue improves reconstruction?
# ============================================================
def experiment_q8():
    """Does including tile color as a retrieval cue improve reconstruction?"""
    print("=" * 60)
    print("Q8: 3-coloring as retrieval cue")
    print("=" * 60)
    
    random.seed(42)
    N = 500
    
    # Store memories at various positions
    memories = {}
    for i in range(N):
        emb = [random.gauss(0, 1) for _ in range(64)]
        proj = project_embedding(emb)
        tile = snap_to_lattice((proj[0], proj[1]))
        color = (tile[0] + 2 * tile[1]) % 3  # 3-coloring
        memories[tile] = {"embedding": emb, "color": color, "content": f"mem_{i}"}
    
    # Query: reconstruct with and without color cue
    spatial_only_correct = 0
    color_cue_correct = 0
    
    for _ in range(200):
        # Pick a stored memory and perturb its embedding
        target_tile = random.choice(list(memories.keys()))
        target_emb = memories[target_tile]["embedding"]
        query_emb = [e + random.gauss(0, 0.3) for e in target_emb]
        
        proj = project_embedding(query_emb)
        query_tile = snap_to_lattice((proj[0], proj[1]))
        query_color = (query_tile[0] + 2 * query_tile[1]) % 3
        
        # Spatial only: find nearest stored tile
        best_spatial = None
        best_dist = float('inf')
        for tile in memories:
            d = math.hypot(tile[0]-query_tile[0], tile[1]-query_tile[1])
            if d < best_dist:
                best_dist = d
                best_spatial = tile
        
        # Color cue: find nearest stored tile WITH SAME COLOR
        best_color = None
        best_color_dist = float('inf')
        for tile, mem in memories.items():
            if mem["color"] == query_color:
                d = math.hypot(tile[0]-query_tile[0], tile[1]-query_tile[1])
                if d < best_color_dist:
                    best_color_dist = d
                    best_color = tile
        
        if best_spatial == target_tile:
            spatial_only_correct += 1
        if best_color == target_tile:
            color_cue_correct += 1
    
    spatial_rate = spatial_only_correct / 200
    color_rate = color_cue_correct / 200
    
    print(f"  Spatial-only accuracy: {spatial_rate:.4f} ({spatial_only_correct}/200)")
    print(f"  Color-cue accuracy: {color_rate:.4f} ({color_cue_correct}/200)")
    
    color_helps = color_rate > spatial_rate
    print(f"  VERDICT: Color cue {'IMPROVES' if color_helps else 'DOES NOT IMPROVE'} retrieval")
    print()
    return {"question": "Q8", "color_helps": color_helps,
            "spatial_rate": spatial_rate, "color_rate": color_rate}

