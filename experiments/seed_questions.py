# Q2: Snap error depends on perturbation angle?
# ============================================================
def experiment_q2():
    """Does snap error rate depend on perturbation angle relative to golden axis?"""
    print("=" * 60)
    print("Q2: Snap error rate vs perturbation angle")
    print("=" * 60)
    
    random.seed(42)
    N = 1000
    
    # Generate embeddings that project to known tiles
    def gen_embedding():
        return [random.gauss(0, 1) for _ in range(64)]
    
    aligned_errors = 0
    aligned_total = 0
    perp_errors = 0
    perp_total = 0
    
    for _ in range(N):
        emb = gen_embedding()
        proj = project_embedding(emb)
        correct_tile = snap_to_lattice((proj[0], proj[1]))
        
        # Aligned perturbation (along projection direction)
        sigma = 0.5
        emb_aligned = [e + sigma * math.cos(2*math.pi*i/64 * PHI) for i, e in enumerate(emb)]
        proj_a = project_embedding(emb_aligned)
        tile_a = snap_to_lattice((proj_a[0], proj_a[1]))
        aligned_total += 1
        if tile_a != correct_tile:
            aligned_errors += 1
        
        # Perpendicular perturbation
        emb_perp = [e + sigma * math.sin(2*math.pi*i/64 * PHI) for i, e in enumerate(emb)]
        proj_p = project_embedding(emb_perp)
        tile_p = snap_to_lattice((proj_p[0], proj_p[1]))
        perp_total += 1
        if tile_p != correct_tile:
            perp_errors += 1
    
    aligned_rate = aligned_errors / aligned_total
    perp_rate = perp_errors / perp_total
    
    print(f"  Aligned perturbation error rate: {aligned_rate:.4f} ({aligned_errors}/{aligned_total})")
    print(f"  Perpendicular perturbation error rate: {perp_rate:.4f} ({perp_errors}/{perp_total})")
    print(f"  Perp/Aligned ratio: {perp_rate/aligned_rate:.4f}" if aligned_rate > 0 else "  No aligned errors")
    
    perp_worse = perp_rate > aligned_rate
    print(f"  VERDICT: Perpendicular errors are {'HIGHER' if perp_worse else 'LOWER'} than aligned")
    print()
    return {"question": "Q2", "perp_worse": perp_worse,
            "aligned_rate": aligned_rate, "perp_rate": perp_rate}

