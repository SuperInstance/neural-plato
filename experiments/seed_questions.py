# Q7: Captured variance depends on principal axis alignment?
# ============================================================
def experiment_q7():
    """Does captured variance depend on alignment with golden projection axis?"""
    print("=" * 60)
    print("Q7: Captured variance vs principal axis alignment")
    print("=" * 60)
    
    random.seed(42)
    N = 500
    
    # Dataset with strong principal axis
    def gen_structured_dataset(axis_angle: float) -> List[List[float]]:
        """Generate 32D data with principal axis at given angle."""
        data = []
        for _ in range(N):
            # First component has 5x variance
            principal = random.gauss(0, 5.0)
            others = [random.gauss(0, 1.0) for _ in range(31)]
            # Rotate the variance into the embedding via the angle
            v = [0.0] * 32
            for i in range(32):
                phase = math.cos(axis_angle + 2*math.pi*i/32)
                v[i] = principal * phase * 0.5 + others[i % len(others)] * 0.5
            data.append(v)
        return data
    
    # Test at different alignments
    angles = [0, math.pi/6, math.pi/3, math.pi/2, 2*math.pi/3, 5*math.pi/6]
    results = []
    
    for angle in angles:
        data = gen_structured_dataset(angle)
        # Compute total variance
        total_var = sum(
            sum((d[i] - sum(d[i] for d in data)/N)**2 for d in data) / N
            for i in range(32)
        )
        # Compute projected variance
        projections = [project_embedding(d) for d in data]
        mean_proj = [sum(p[i] for p in projections)/N for i in range(2)]
        proj_var = sum(
            (p[0]-mean_proj[0])**2 + (p[1]-mean_proj[1])**2
            for p in projections
        ) / N
        
        capture = proj_var / total_var if total_var > 0 else 0
        results.append((angle, capture))
        print(f"  Angle {math.degrees(angle):6.1f}°: capture = {capture:.6f}")
    
    best_angle = max(results, key=lambda x: x[1])
    worst_angle = min(results, key=lambda x: x[1])
    
    print(f"\n  Best alignment: {math.degrees(best_angle[0]):.1f}° (capture {best_angle[1]:.6f})")
    print(f"  Worst alignment: {math.degrees(worst_angle[0]):.1f}° (capture {worst_angle[1]:.6f})")
    print(f"  Ratio: {best_angle[1]/worst_angle[1]:.4f}")
    
    alignment_matters = best_angle[1] > worst_angle[1] * 1.05
    print(f"  VERDICT: Principal axis alignment {'MATTERS' if alignment_matters else 'DOES NOT MATTER'}")
    print()
    return {"question": "Q7", "alignment_matters": alignment_matters,
            "best": best_angle, "worst": worst_angle}

