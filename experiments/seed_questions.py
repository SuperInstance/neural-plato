import math
import json
import random
from typing import List, Tuple, Dict

PHI = (1 + math.sqrt(5)) / 2
INV_PHI = 1.0 / PHI

def fibonacci_bit(index: int) -> bool:
    current = int(index * INV_PHI)
    nxt = int((index + 1) * INV_PHI)
    return nxt != current

def tile_bit(pos: Tuple[int, int]) -> bool:
    q = (pos[0] * 0x9E3779B9) & 0xFFFFFFFF
    mixed = abs((q ^ (pos[1] & 0xFFFFFFFF)) & 0xFFFFFFFF)
    idx = mixed % 1000
    return fibonacci_bit(idx)

def project_embedding(embedding: List[float], target_dim: int = 2,
                      angle_mult: float = None) -> List[float]:
    """Project high-D to low-D. angle_mult=PHI for golden, other for comparison."""
    n = len(embedding)
    if angle_mult is None:
        angle_mult = PHI
    result = []
    for d in range(target_dim):
        val = 0.0
        for i in range(n):
            angle = 2 * math.pi * i / n * angle_mult ** (d + 1)
            val += embedding[i] * math.cos(angle) / math.sqrt(n)
        result.append(val)
    return result

def snap_to_lattice(pos: Tuple[float, float]) -> Tuple[int, int]:
    return (round(pos[0] / PHI), round(pos[1] / PHI))

def cosine_sim(a: List[float], b: List[float]) -> float:
    dot = sum(x*y for x, y in zip(a, b))
    na = math.sqrt(sum(x*x for x in a))
    nb = math.sqrt(sum(x*x for x in b))
    return dot / (na * nb) if na > 0 and nb > 0 else 0.0

